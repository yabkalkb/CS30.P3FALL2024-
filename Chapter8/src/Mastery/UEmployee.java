package Mastery;

public class UEmployee {
    private String name;
    private double salary;

    // Constructor for UEmployee
    public UEmployee(String name, double salary) {
        this.name = name;
        this.salary = salary;
    }

    // Getter for employee's name
    public String getName() {
        return name;
    }

    // Getter for employee's salary
    public double getSalary() {
        return salary;
    }
}

